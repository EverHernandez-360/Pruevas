    private void Bt_agregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Bt_agregarActionPerformed
        String nombre = Text1.getText().trim();
    String marca = Text2.getText().trim();
    String modelo = Text3.getText().trim();
    String existenciaStr = Text4.getText().trim();
    String precioStr = Text5.getText().trim();

    // Validaci√≥n b√°sica
    if (nombre.isEmpty() || marca.isEmpty() || modelo.isEmpty() || existenciaStr.isEmpty() || precioStr.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Todos los campos deben estar llenos.");
        return;
    }

    try {
        int existencias = Integer.parseInt(existenciaStr);
        double precio = Double.parseDouble(precioStr);

        // Armar l√≠nea
        String nuevaLinea = nombre + "-" + marca + "-" + modelo + "-" + existencias + "-" + precio;

        // Agregar al JTextArea
        Contenido.append(nuevaLinea + "\n");

        // Limpiar campos
        Text1.setText("");
        Text2.setText("");
        Text3.setText("");
        Text4.setText("");
        Text5.setText("");

    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "Existencias debe ser un n√∫mero entero y precio un n√∫mero decimal.");
    }
    }//GEN-LAST:event_Bt_agregarActionPerformed

        public void muestraContenido(String name) throws IOException {
        rutaArchivoActual = name;

        BufferedReader reader = new BufferedReader(new FileReader(name));
        Contenido.setText("");
        String linea;
        while ((linea = reader.readLine()) != null) {
            Contenido.append(linea + "\n");
        }
        reader.close();

        controlador.cargarDesdeArchivo(name);     
}
