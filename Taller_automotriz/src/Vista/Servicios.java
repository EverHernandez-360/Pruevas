    private void Bt_AbrirarchivoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Bt_AbrirarchivoActionPerformed
    JFileChooser fc = new JFileChooser();
        fc.setFileSelectionMode(JFileChooser.FILES_ONLY);//Indicamos que abra solamente archivos
        
        FileNameExtensionFilter filtro = new FileNameExtensionFilter("Archivos de texto", "txt", "tms");//Solamente acepta archivos de estas extenciones
        fc.setFileFilter(filtro);
        
        int res = fc.showOpenDialog(this);
        if(res != JFileChooser.CANCEL_OPTION){// Abrio un archivo
            File name = fc.getSelectedFile();
            
            if((name == null) || name.getName().equals("")){
                JOptionPane.showMessageDialog(this, "Error de archivo");
            }else {
                Ruta.setText(name.getAbsolutePath());//Nos muestra la ruta completa de donde esta el archivo
                try {
                    muestraContenido(name.getAbsolutePath());
                } catch (IOException ex) {
                    Logger.getLogger(Repuestos.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        }
    }//GEN-LAST:event_Bt_AbrirarchivoActionPerformed

    private void Bt_GuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Bt_GuardarActionPerformed
         if (rutaArchivoActual == null) {
        JOptionPane.showMessageDialog(this, "Primero debes abrir un archivo.");
        return;
    }

    String contenido = Contenido.getText();
    if (contenido.isEmpty()) {
        JOptionPane.showMessageDialog(this, "No hay datos que guardar.");
        return;
    }

    try {
        // 1. Guardar el archivo
        BufferedWriter writer = new BufferedWriter(new FileWriter(rutaArchivoActual));
        writer.write(contenido);
        writer.close();

        controlador.actualizarInventarioDesdeTexto(contenido);

        StringBuilder resumen = new StringBuilder("✅ Servicios guardados:\n");
        for (int i = 0; i < servicioInventario.getCantidad(); i++) {
            Mo_servicios s = servicioInventario.getServicios()[i];
            resumen.append("- ").append(s.getNombre2())
                    .append(" → Total: ").append(s.getTotalprecio()).append("\n");
        }

        JOptionPane.showMessageDialog(this, resumen.toString());

    } catch (IOException e) {
        JOptionPane.showMessageDialog(this, "Error al guardar: " + e.getMessage());
    }
    }//GEN-LAST:event_Bt_GuardarActionPerformed

