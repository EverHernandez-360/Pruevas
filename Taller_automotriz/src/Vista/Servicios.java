    private void Bt_agregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Bt_agregarActionPerformed
    String nombre = Text1.getText().trim();
    String marca = Text2.getText().trim();
    String modelo = Text3.getText().trim();
    String listaRepuestos = Text4.getText().trim();
    String manoObraStr = Text5.getText().trim();

    if (nombre.isEmpty() || marca.isEmpty() || modelo.isEmpty() || listaRepuestos.isEmpty() || manoObraStr.isEmpty()) {
        JOptionPane.showMessageDialog(this, "⚠️ Todos los campos deben estar llenos.");
        return;
    }

    try {
        int preMano = Integer.parseInt(manoObraStr);

        String nuevaLinea = nombre + "-" + marca + "-" + modelo + "-" + listaRepuestos + "-" + preMano;
        Contenido.append(nuevaLinea + "\n");

        Text1.setText(""); Text2.setText(""); Text3.setText("");
        Text4.setText(""); Text5.setText("");

    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "❌ Precio de mano de obra inválido.");
    }    
    }//GEN-LAST:event_Bt_agregarActionPerformed
 
    public void muestraContenido(String name) throws IOException {
        rutaArchivoActual = name;

        BufferedReader reader = new BufferedReader(new FileReader(name));
        Contenido.setText("");
        String linea;
        while ((linea = reader.readLine()) != null) {
            Contenido.append(linea + "\n");//Contenido.append(linea).append("\n");
        }
        reader.close();

        
        controlador.actualizarInventarioDesdeTexto(Contenido.getText()); 
    }    

